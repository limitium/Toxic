<?php

namespace Tox\ParserBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Tox\ParserBundle\Entity\PatternType
 */
class PatternType
{
    /**
     * @var integer $id
     */
    private $id;

    /**
     * @var string $name
     */
    private $name;

    /**
     * @var boolean $is_meta
     */
    private $is_meta;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * Get name
     *
     * @return string 
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set is_meta
     *
     * @param boolean $isMeta
     */
    public function setIsMeta($isMeta)
    {
        $this->is_meta = $isMeta;
    }

    /**
     * Get is_meta
     *
     * @return boolean 
     */
    public function getIsMeta()
    {
        return $this->is_meta;
    }
}